namespace TaskbarTools;

/// <summary>
/// Values for areas in the taskbar.
/// </summary>
internal enum TaskbarAreas
{
    /// <summary>
    /// The tray.
    /// </summary>
    TrayArea = 0x01,

    /// <summary>
    /// The notification area.
    /// </summary>
    NotificationArea = 0x02,

    /// <summary>
    /// The icon area.
    /// </summary>
    IconArea = 0x04,
}
